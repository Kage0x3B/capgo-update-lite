<h1>capgo-update-lite</h1>
<p>Self-hosted OTA update server. API-only.</p>
<ul>
    <li><code>POST /updates</code> — plugin check-in</li>
    <li><code>POST /stats</code> — plugin telemetry</li>
    <li><code>POST /admin/bundles/init</code> — presigned upload init</li>
    <li><code>POST /admin/bundles/commit</code> — finalize upload</li>
</ul>
