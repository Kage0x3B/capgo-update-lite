	'ping',
	'delete',
	'reset',
	'set',
	'get',
	'set_fail',
	'update_fail',
	'download_fail',
	'windows_path_fail',
	'canonical_path_fail',
	'directory_path_fail',
	'unzip_fail',
	'low_mem_fail',
	'download_0',
	'download_10',
