import * as v from 'valibot';

const REVERSE_DOMAIN = /^[a-z0-9]+(\.[\w-]+)+$/i;
const DEVICE_ID = /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i;

// Verbatim copy of capgo-backend/.../plugins/stats_actions.ts:4-65. Must stay
// in lock-step with upstream or we'll reject events the plugin emits.
